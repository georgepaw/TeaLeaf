//CRC32

// Byte-boundary alignment issues
#define ALIGN_SIZE          0x08UL              // Align at an 8-byte boundary
#define ALIGN_MASK          (ALIGN_SIZE - 1)    // Bitmask for 8-byte bound addresses

#define CALC_CRC32(_mm_crc32_u, crc, type, data, num_bytes)                                \
  do                                                                                       \
  {                                                                                        \
    for (; num_bytes >= sizeof(type); num_bytes -= sizeof(type), data += sizeof(type))     \
    {                                                                                      \
        (crc) = _mm_crc32_u((crc), *(type *) (data));                                      \
    }                                                                                      \
  } while(0)

uint32_t crc32_hardware(uint32_t crc, const uint8_t * data, size_t num_bytes)
{
  // Align the input to the word boundary
  for (; (num_bytes > 0) && ((size_t)data & ALIGN_MASK); num_bytes--, data++) {
    crc = _mm_crc32_u8(crc, *data);
  }

  //run hardware crc instructions for remaining bytes
  //do as much as possible with each instruction
#if defined(__x86_64__) || defined(_M_X64)
  CALC_CRC32(_mm_crc32_u64, crc, uint64_t, data, num_bytes);
#endif
  CALC_CRC32(_mm_crc32_u32, crc, uint32_t, data, num_bytes);
  CALC_CRC32(_mm_crc32_u16, crc, uint16_t, data, num_bytes);
  CALC_CRC32(_mm_crc32_u8, crc, uint8_t, data, num_bytes);
  return crc;
}

static uint32_t generate_crc32_bits(uint32_t * a_cols, double * a_non_zeros)
{
  uint32_t crc = 0xFFFFFFFF;
  uint32_t bytes;
  uint32_t masks[5];
  //remove masks
  for(int i = 0; i < 5; i++)
  {
    masks[i] = a_cols[i] & 0xFF000000;
    a_cols[i] &= 0x00FFFFFF;
  }
  bytes = 20;
  crc = crc32_hardware(crc, (uint8_t*)a_cols, bytes);
  bytes = 40;
  crc = crc32_hardware(crc, (uint8_t*)a_non_zeros, bytes);
  //restore masks
  for(int i = 0; i < 5; i++)
  {
    a_cols[i] += masks[i];
  }
  return crc;
}

static uint8_t check_correct_crc32_bits(uint32_t * a_cols, double * a_non_zeros)
{
  //get the CRC and recalculate to check it's correct
  uint32_t prev_crc = (a_cols[0] & 0xFF000000)
                    + ((a_cols[1] & 0xFF000000)>>8)
                    + ((a_cols[2] & 0xFF000000)>>16)
                    + (a_cols[3] >> 24);
  return prev_crc == generate_crc32_bits(a_cols, a_non_zeros);
}


