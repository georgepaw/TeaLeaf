    crc = crc8_table[crc ^ *byte_array++];



static inline uint8_t generate_crc8_bits(const uint32_t * restrict a_cols, const double * restrict a_non_zeros)
{
  uint8_t crc = 0xFF;
  uint32_t bytes;
  const uint8_t *byte_array;

  //bytes is 3 because we are ignoring the top bit which containts the CRC data
  bytes = 3;
  CALC_CRC8(crc, a_cols, bytes);
  bytes = 3;
  CALC_CRC8(crc, &a_cols[1], bytes);
  bytes = 3;
  CALC_CRC8(crc, &a_cols[2], bytes);
  bytes = 3;
  CALC_CRC8(crc, &a_cols[3], bytes);
  bytes = 3;
  CALC_CRC8(crc, &a_cols[4], bytes);
  bytes = 40;
  CALC_CRC8(crc, a_non_zeros, bytes);
  return crc;
}

static inline uint8_t check_correct_crc8_bits(const uint32_t * restrict a_cols, const double * restrict a_non_zeros)
{
  //get the CRC and recalculate to check it's correct
  return a_cols[0] >> 24 == generate_crc8_bits(a_cols, a_non_zeros);
}

